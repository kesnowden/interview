package interview;

public class Sum {
	/**
	 * Modify this method so it will return the sum of
	 * all multiples of three between 0 and
	 * the value of input inclusive. So for an input of 3
	 * the method should return 3. For an input of 6, 7, or 8,
	 * the method should return 9 (3 + 6). 
	 */
	public int multiplesOfThree(int input) {
		int k = input / 3;
		int count = (k * (k + 1)) / 2;
		int sum = count * 3;
		return sum;
	}

}
